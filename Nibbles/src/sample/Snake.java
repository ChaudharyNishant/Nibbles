package sample;

public class Snake
{
	int iIndex, jIndex;
	
	Snake(int i, int j)
	{
		iIndex = i;
		jIndex = j;
	}
	
	Snake()
	{
		iIndex = 0;
		jIndex = 0;
	}
}

